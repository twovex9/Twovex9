import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const rawPath = path.join(__dirname, "gemeenten-user-paste.txt");

const raw = fs.readFileSync(rawPath, "utf8");

function expandLine(line) {
  var t = line.replace(/\t+/g, " ").replace(/\s+/g, " ").trim();
  if (!t) return [];
  if (t === "Maasdriel Reusel-De Mierden") return ["Maasdriel", "Reusel-De Mierden"];
  if (t === "RidderkerkTerschelling") return ["Ridderkerk", "Terschelling"];
  if (t === "Aalsmeer Aalten") return ["Aalsmeer", "Aalten"];
  if (t === "Leiderdorp Hattem") return ["Leiderdorp", "Hattem"];
  if (t === "Oisterwijk Beek") return ["Oisterwijk", "Beek"];
  return [t];
}

var chunks = raw.split(/\r?\n/);
var all = [];
chunks.forEach(function (line) {
  expandLine(line).forEach(function (x) {
    all.push(x);
  });
});

var byKey = new Map();
all.forEach(function (name) {
  var k = name.toLowerCase();
  if (!byKey.has(k)) byKey.set(k, name);
});

var names = Array.from(byKey.values());
names.sort(function (a, b) {
  return a.localeCompare(b, "nl", { sensitivity: "base" });
});

var out =
  "/* eslint-disable */\n" +
  "/* Generated by scripts/build-gemeenten-bulk.mjs — unique count: " +
  names.length +
  " */\n" +
  "(function (g) {\n" +
  '  "use strict";\n' +
  "  g.GEMEENTEN_STAM_NAMEN = " +
  JSON.stringify(names, null, 2) +
  ";\n" +
  "})(typeof window !== \"undefined\" ? window : globalThis);\n";

fs.writeFileSync(path.join(root, "gemeenten-bulk.js"), out, "utf8");
console.log("Wrote gemeenten-bulk.js with", names.length, "unique names");
